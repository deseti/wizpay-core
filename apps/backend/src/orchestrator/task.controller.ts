import {
  BadGatewayException,
  BadRequestException,
  Body,
  Controller,
  Get,
  Param,
  ParseIntPipe,
  ParseUUIDPipe,
  Post,
  Query,
  UnauthorizedException,
  UsePipes,
  ValidationPipe,
} from '@nestjs/common';
import { OrchestratorService } from './orchestrator.service';
import { PayrollInitService } from './payroll-init.service';
import { CreateTaskDto } from '../task/dto/create-task.dto';
import { TaskService } from '../task/task.service';
import { TaskEmployeeBreakdownService } from '../task/task-employee-breakdown.service';
import { TaskPayrollHistoryService } from '../task/task-payroll-history.service';
import { CircleService } from '../adapters/circle.service';
import { TaskType } from '../task/task-type.enum';
import type { ReportTaskUnitInput } from '../task/task.types';

@Controller('tasks')
export class TaskController {
  constructor(
    private readonly orchestratorService: OrchestratorService,
    private readonly taskService: TaskService,
    private readonly taskEmployeeBreakdownService: TaskEmployeeBreakdownService,
    private readonly taskPayrollHistoryService: TaskPayrollHistoryService,
    private readonly payrollInitService: PayrollInitService,
    private readonly circleService: CircleService,
  ) {}

  @Post('fx/quote')
  async quoteFx(@Body() payload: Record<string, unknown>) {
    const sourceCurrency =
      typeof payload.sourceCurrency === 'string' ? payload.sourceCurrency : '';
    const targetCurrency =
      typeof payload.targetCurrency === 'string' ? payload.targetCurrency : '';
    const sourceAmount =
      typeof payload.sourceAmount === 'string' ? payload.sourceAmount : '';
    const recipientAddress =
      typeof payload.recipientAddress === 'string'
        ? payload.recipientAddress
        : undefined;

    if (!sourceCurrency || !targetCurrency || !sourceAmount) {
      throw new BadRequestException(
        'Missing required fields: sourceCurrency, targetCurrency, sourceAmount',
      );
    }

    try {
      return {
        data: await this.circleService.getQuote({
          sourceCurrency,
          targetCurrency,
          sourceAmount,
          recipientAddress,
        }),
      };
    } catch (error) {
      const message =
        error instanceof Error ? error.message : 'Unable to fetch FX quote';

      if (message.includes('401')) {
        throw new UnauthorizedException(message);
      }

      throw new BadGatewayException(message);
    }
  }

  @Post('fx/execute')
  async executeFx(@Body() payload: Record<string, unknown>) {
    return {
      data: await this.orchestratorService.handleTask(
        TaskType.FX,
        payload ?? {},
      ),
    };
  }

  /**
   * POST /tasks/payroll/init — Validate and batch a payroll run before execution.
   *
   * Frontend sends the current payroll draft and receives a normalized plan
   * generated by backend payroll services. This keeps validation, batching,
   * and reference-id expansion in one place before any wallet confirmation.
   */
  @Post('payroll/init')
  async initPayroll(@Body() payload: Record<string, unknown>) {
    return {
      data: await this.payrollInitService.prepare(payload ?? {}),
    };
  }

  @Post('swap/init')
  async initSwap(@Body() payload: Record<string, unknown>) {
    return {
      data: await this.taskService.createSwapTask(payload ?? {}),
    };
  }

  @Post('liquidity/init')
  async initLiquidity(@Body() payload: Record<string, unknown>) {
    return {
      data: await this.taskService.createLiquidityTask(payload ?? {}),
    };
  }

  @Post(':taskId/units/:unitId/report')
  async reportTaskUnit(
    @Param('taskId', new ParseUUIDPipe()) taskId: string,
    @Param('unitId', new ParseUUIDPipe()) unitId: string,
    @Body() body: ReportTaskUnitInput,
  ) {
    return {
      data: await this.taskService.reportUnit(taskId, unitId, body),
    };
  }

  /**
   * POST /tasks — Create and enqueue a new task.
   *
   * This is the ONLY entry point for frontend task submission.
   * Frontend sends:
   *   { "type": "payroll", "payload": { ... } }
   *
   * Backend handles:
   *   - DTO validation (class-validator)
   *   - Task creation + state machine
   *   - Queue routing + agent execution
   */
  @Post()
  @UsePipes(
    new ValidationPipe({
      exceptionFactory: (errors) =>
        new BadRequestException({
          code: 'VALIDATION_FAILED',
          errors: flattenValidationErrors(errors),
          message: 'Request validation failed',
        }),
      whitelist: true,
      forbidNonWhitelisted: true,
      transform: true,
    }),
  )
  async createTask(@Body() body: CreateTaskDto) {
    return {
      data: await this.orchestratorService.handleTask(
        body.type,
        body.payload ?? {},
      ),
    };
  }

  /**
   * GET /tasks — List tasks with optional filters.
   *
   * Query params:
   *   type       — filter by task type (payroll, swap, bridge, liquidity, fx)
   *   status     — filter by status (executed, failed, in_progress, …)
   *   wallet     — filter by wallet/recipient address stored in metadata
   *   limit      — max results (default 50, max 200)
   *   offset     — pagination offset (default 0)
   */
  @Get()
  async listTasks(
    @Query('type') type?: string,
    @Query('status') status?: string,
    @Query('wallet') walletAddress?: string,
    @Query('limit', new ParseIntPipe({ optional: true })) limit?: number,
    @Query('offset', new ParseIntPipe({ optional: true })) offset?: number,
  ) {
    return {
      data: await this.taskService.getTaskList({
        type,
        status,
        walletAddress,
        limit,
        offset,
      }),
    };
  }

  /**
   * GET /tasks/payroll/history?wallet=0x... — Wallet-scoped payroll history.
   *
   * Returns confirmed payroll batch events and employee-level payments using
   * backend chain reads so overview cards and recent payroll tables do not
   * scan logs in the browser. Includes legacy contract coverage.
   */
  @Get('payroll/history')
  async getPayrollHistory(@Query('wallet') walletAddress?: string) {
    return {
      data: await this.taskPayrollHistoryService.getWalletPayrollHistory(
        walletAddress,
      ),
    };
  }

  /**
   * GET /tasks/:id — Poll task status.
   *
   * Frontend polls this endpoint to track progress.
   * Returns the full task with logs for fine-grained progress display.
   */
  @Get(':id')
  async getTask(@Param('id', new ParseUUIDPipe()) id: string) {
    return {
      data: await this.taskService.getTaskById(id),
    };
  }

  /**
   * GET /tasks/:id/employee-breakdown — Return confirmed employee-level payroll payments.
   *
   * Frontend uses this endpoint to render recent payroll rows without scanning
   * chain logs or transaction receipts in the browser.
   */
  @Get(':id/employee-breakdown')
  async getEmployeeBreakdown(@Param('id', new ParseUUIDPipe()) id: string) {
    return {
      data: await this.taskEmployeeBreakdownService.getPayrollEmployeeBreakdown(
        id,
      ),
    };
  }
}

function flattenValidationErrors(
  errors: Array<{
    children?: unknown[];
    constraints?: Record<string, string>;
    property: string;
  }>,
  parentPath = '',
): Array<{ field: string; messages: string[] }> {
  return errors.flatMap((error) => {
    const field = parentPath
      ? `${parentPath}.${error.property}`
      : error.property;
    const messages = Object.values(error.constraints ?? {});
    const children = Array.isArray(error.children)
      ? flattenValidationErrors(
          error.children as Array<{
            children?: unknown[];
            constraints?: Record<string, string>;
            property: string;
          }>,
          field,
        )
      : [];

    return [...(messages.length > 0 ? [{ field, messages }] : []), ...children];
  });
}
